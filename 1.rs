use std::str::FromStr;

fn iter_sum(modules: Vec<i32>, func: F) -> i32
    where F: Fn(&i32) -> i32 {
    modules
        .iter()
        .map(func)
        .sum()
}

fn fuel(&x: &i32) -> i32 {
    x / 3 - 2
}

fn more_fuel(&module: &i32) -> i32 {
    let mut f = module;
    let mut total = 0;
    loop {
        f = fuel(f);
        if f <= 0 {
            break;
        } else {
            total += f;
        }
    }
    total
}

